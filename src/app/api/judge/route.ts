import { NextRequest, NextResponse } from "next/server";
import config from "@/lib/config";
import { callLLM, flushSpans, scoreTrace } from "@/lib/llm";

export const maxDuration = 120;

interface GenerationEntry {
  modelName: string;
  output: string;
}

interface JudgeRequestBody {
  url: string;
  productRequirements: string;
  judgePrompt: string;
  generations: Record<string, GenerationEntry>;
  /** When provided, run only this single judge model. */
  judgeId?: string;
}

interface JudgeScore {
  score: number;
  feedback: string;
}

interface JudgeResult {
  success: boolean;
  score?: number;
  feedback?: string;
  selfEvaluation?: boolean;
  langfuseTraceId?: string;
  error?: string;
}

/** Extract JSON from a string that may be wrapped in markdown code fences. */
function parseJudgeResponse(text: string): JudgeScore | null {
  // Try raw parse first
  try {
    return JSON.parse(text) as JudgeScore;
  } catch {
    // Strip markdown code fences and retry
    const match = text.match(/```(?:json)?\s*([\s\S]*?)\s*```/);
    if (match) {
      try {
        return JSON.parse(match[1]) as JudgeScore;
      } catch {
        // fall through
      }
    }
    // Last resort: find the first {...} block
    const braceMatch = text.match(/\{[\s\S]*\}/);
    if (braceMatch) {
      try {
        return JSON.parse(braceMatch[0]) as JudgeScore;
      } catch {
        // fall through
      }
    }
    return null;
  }
}

export async function POST(req: NextRequest) {
  try {
  let body: JudgeRequestBody;
  try {
    body = await req.json();
  } catch {
    return NextResponse.json({ error: "Invalid JSON body" }, { status: 400 });
  }

  const { url, productRequirements, judgePrompt, generations, judgeId } = body;
  if (!url || !productRequirements || !judgePrompt || !generations) {
    return NextResponse.json(
      { error: "Missing required fields: url, productRequirements, judgePrompt, generations" },
      { status: 400 }
    );
  }

  const judgesToRun = judgeId
    ? config.judges.filter((j) => j.id === judgeId)
    : config.judges;

  if (judgesToRun.length === 0) {
    return NextResponse.json({ error: `Unknown judgeId: ${judgeId}` }, { status: 400 });
  }

  // Build all (judge × generator) pairs
  type Task = { judgeId: string; generatorId: string; promise: Promise<JudgeResult> };

  const tasks: Task[] = [];

  for (const judge of judgesToRun) {
    for (const [generatorId, generation] of Object.entries(generations)) {
      // Find the generator model config to check for self-evaluation
      const generatorConfig = config.generators.find((g) => g.id === generatorId);
      const selfEvaluation =
        generatorConfig !== undefined &&
        generatorConfig.provider === judge.provider &&
        generatorConfig.model === judge.model;

      const userPrompt = `You are evaluating the quality of AI-generated test cases.

Application under test: ${url}

Product Requirements:
${productRequirements}

Test Cases to Evaluate (generated by ${generation.modelName}):
${generation.output}

Score the test cases from 0 to 5 where:
0 = Completely unusable
1 = Major gaps, few usable test cases
2 = Some coverage but significant gaps
3 = Decent coverage, misses edge cases
4 = Good coverage including edge cases
5 = Excellent, comprehensive, production-ready

Respond in this exact JSON format and nothing else:
{
  "score": <number 0-5>,
  "feedback": "<detailed explanation: what's good, what's missing, what could be improved>"
}`;

      const promise = callLLM({
        provider: judge.provider,
        model: judge.model,
        systemPrompt: judgePrompt,
        userPrompt,
        maxTokens: judge.maxTokens,
        temperature: judge.temperature,
        traceContext: {
          traceName: `judge: ${judge.id} → ${generatorId}`,
          role: "judge",
          url,
          tags: ["judge", judge.id, generatorId],
        },
      }).then((res): JudgeResult => {
        const parsed = parseJudgeResponse(res.text);
        if (parsed) {
          return {
            success: true,
            score: parsed.score,
            feedback: parsed.feedback,
            selfEvaluation,
            langfuseTraceId: res.traceId,
          };
        }
        // Parsing failed — return raw text as feedback, score null
        return {
          success: true,
          score: undefined,
          feedback: res.text,
          selfEvaluation,
          langfuseTraceId: res.traceId,
        };
      });

      tasks.push({ judgeId: judge.id, generatorId, promise });
    }
  }

  const settled = await Promise.allSettled(tasks.map((t) => t.promise));

  // Assemble nested results[judgeId][generatorId]
  const results: Record<string, Record<string, JudgeResult>> = {};

  settled.forEach((outcome, i) => {
    const { judgeId, generatorId } = tasks[i];
    if (!results[judgeId]) results[judgeId] = {};

    if (outcome.status === "fulfilled") {
      results[judgeId][generatorId] = outcome.value;
    } else {
      results[judgeId][generatorId] = {
        success: false,
        error:
          outcome.reason instanceof Error
            ? outcome.reason.message
            : String(outcome.reason),
      };
    }
  });

  // Attach judge scores to Langfuse traces
  const scoreCalls: Array<Promise<unknown>> = [];
  for (const [judgeId, genMap] of Object.entries(results)) {
    for (const [genId, result] of Object.entries(genMap)) {
      if (result.success && result.score !== undefined && result.langfuseTraceId) {
        scoreCalls.push(
          scoreTrace({
            traceId: result.langfuseTraceId,
            name: `judge-score:${judgeId}→${genId}`,
            value: result.score,
            comment: result.feedback,
            source: "llm_judge",
          })
        );
      }
    }
  }
  await Promise.allSettled(scoreCalls);

  await flushSpans();
  return NextResponse.json({ results });
  } catch (err) {
    const message = err instanceof Error ? err.message : String(err);
    return NextResponse.json({ error: message }, { status: 500 });
  }
}

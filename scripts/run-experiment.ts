// ── Public types ───────────────────────────────────────────────────────────────

export interface ExperimentConfig {
  url: string;
  testMethodology: string;
  productRequirements: string;
  judgePrompt: string;
  revisionNotes: string;
  /** Optional paths to PNG/JPG/WebP screenshots — read from disk and sent to all models. */
  imagePaths?: string[];
}

interface GenerationResult {
  success: boolean;
  output?: string;
  tokenUsage?: { input: number; output: number };
  latencyMs?: number;
  traceId?: string;
  error?: string;
}

interface JudgeScoreResult {
  success: boolean;
  score?: number;
  feedback?: string;
  selfEvaluation?: boolean;
  traceId?: string;
  error?: string;
}

export interface ExperimentResult {
  revisionNumber: number;
  slug: string;
  url: string;
  generations: Record<string, GenerationResult>;
  /** judgeScores[judgeId][generatorId] */
  judgeScores: Record<string, Record<string, JudgeScoreResult>>;
}

// ── Helpers ────────────────────────────────────────────────────────────────────

export function resolvePrompt(value: string): string {
  if (value.startsWith("file:")) {
    const filePath = value.slice(5);
    const resolved = path.resolve(process.cwd(), filePath);
    if (!existsSync(resolved)) {
      throw new Error(`Prompt file not found: ${resolved}`);
    }
    console.log(`Loaded prompt from: ${filePath}`);
    return readFileSync(resolved, "utf-8");
  }
  return value;
}

function mimeType(filePath: string): string {
  switch (path.extname(filePath).toLowerCase()) {
    case ".png":  return "image/png";
    case ".jpg":
    case ".jpeg": return "image/jpeg";
    case ".webp": return "image/webp";
    default: throw new Error(`Unsupported image format: ${filePath} — use .png, .jpg, or .webp`);
  }
}

async function loadImages(imagePaths: string[]): Promise<LLMImage[]> {
  return Promise.all(
    imagePaths.map(async (p) => {
      const abs = path.resolve(p);
      const data = await fs.readFile(abs);
      return { base64: data.toString("base64"), mimeType: mimeType(abs) };
    })
  );
}

/**
 * Exactly matches the userPrompt assembled in src/app/api/generate/route.ts
 */
function buildGeneratorUserPrompt(
  url: string,
  productRequirements: string,
  hasImages: boolean
): string {
  if (hasImages) {
    return `Application under test: ${url}

[Screenshots of the application UI are attached above for visual context]

Product Requirements:
${productRequirements}

Based on the above requirements and the attached screenshots, generate comprehensive test cases for this application.`;
  }
  return `Application under test: ${url}

Product Requirements:
${productRequirements}

Based on the above requirements, generate comprehensive test cases for this application.`;
}

/**
 * Exactly matches the userPrompt assembled in src/app/api/judge/route.ts
 */
function buildJudgeUserPrompt(
  url: string,
  productRequirements: string,
  generatorName: string,
  output: string,
  hasImages: boolean
): string {
  const imageNote = hasImages
    ? "\n\n[Screenshots of the application are attached above for visual context]"
    : "";

  return `You are evaluating the quality of AI-generated test cases.

Application under test: ${url}${imageNote}

Product Requirements:
${productRequirements}

Test Cases to Evaluate (generated by ${generatorName}):
${output}

Score the test cases from 0 to 5 where:
0 = Completely unusable
1 = Major gaps, few usable test cases
2 = Some coverage but significant gaps
3 = Decent coverage, misses edge cases
4 = Good coverage including edge cases
5 = Excellent, comprehensive, production-ready

Respond in this exact JSON format and nothing else:
{
  "score": <number 0-5>,
  "feedback": "<detailed explanation: what's good, what's missing, what could be improved>"
}`;
}

// ── Core pipeline ──────────────────────────────────────────────────────────────

export async function runExperiment(
  expConfig: ExperimentConfig
): Promise<ExperimentResult> {
  const { url, testMethodology, productRequirements, judgePrompt, revisionNotes, imagePaths } =
    expConfig;

  // Load images from disk (empty array if none configured)
  const images: LLMImage[] = imagePaths?.length ? await loadImages(imagePaths) : [];
  const hasImages = images.length > 0;

  const userPrompt = buildGeneratorUserPrompt(url, productRequirements, hasImages);

  // ── Phase 1: Generate ────────────────────────────────────────────────────────

  console.log(`\nGenerating with ${appConfig.generators.length} model(s) in parallel…`);

  const genTasks = appConfig.generators.map(
    (model): Promise<[string, GenerationResult]> =>
      callLLM({
        provider: model.provider,
        model: model.model,
        systemPrompt: testMethodology,
        userPrompt,
        maxTokens: model.maxTokens,
        temperature: model.temperature,
        images: hasImages ? images : undefined,
        traceContext: {
          traceName: `generate: ${model.id}`,
          role: "generator",
          url,
          tags: ["generate", model.id],
        },
      })
        .then(
          (res): [string, GenerationResult] => [
            model.id,
            {
              success: true,
              output: res.text,
              tokenUsage: res.tokenUsage,
              latencyMs: res.latencyMs,
              traceId: res.traceId,
            },
          ]
        )
        .catch(
          (err): [string, GenerationResult] => [
            model.id,
            { success: false, error: err instanceof Error ? err.message : String(err) },
          ]
        )
  );

  const genEntries = await Promise.all(genTasks);
  const generations: Record<string, GenerationResult> = Object.fromEntries(genEntries);

  // Report generation results
  for (const model of appConfig.generators) {
    const r = generations[model.id];
    if (r.success) {
      const tokens = (r.tokenUsage!.input + r.tokenUsage!.output).toLocaleString();
      console.log(
        `  ✓ ${model.name} — ${tokens} tokens, ${(r.latencyMs! / 1000).toFixed(1)}s, ${r.output!.length} chars`
      );
    } else {
      console.log(`  ✗ ${model.name} — FAILED: ${r.error}`);
    }
  }

  // ── Phase 2: Save revision ───────────────────────────────────────────────────

  const configSnapshot: ConfigSnapshot = {
    generators: appConfig.generators.map((g) => ({
      id: g.id,
      name: g.name,
      model: g.model,
      provider: g.provider,
    })),
    judges: appConfig.judges.map((j) => ({
      id: j.id,
      name: j.name,
      model: j.model,
      provider: j.provider,
    })),
  };

  const savedGenerations = Object.fromEntries(
    genEntries
      .filter(([, r]) => r.success && r.output !== undefined)
      .map(([id, r]) => [
        id,
        {
          output: r.output!,
          tokenUsage: r.tokenUsage!,
          latencyMs: r.latencyMs!,
          langfuseTraceId: r.traceId!,
        },
      ])
  );

  const revisionNumber = await saveRevision({
    url,
    prompts: { testMethodology, productRequirements, judgePrompt },
    revisionNotes,
    images: [],
    configSnapshot,
    generations: savedGenerations,
    scores: {
      human: Object.fromEntries(appConfig.generators.map((g) => [g.id, null])),
      judges: {},
    },
  });

  const slug = urlToSlug(url);
  console.log(`\n  Revision ${revisionNumber} saved → data/${slug}.json`);

  // ── Phase 3: Judge ───────────────────────────────────────────────────────────

  const generatorsWithOutput = appConfig.generators.filter(
    (g) => generations[g.id]?.success && generations[g.id]?.output
  );

  if (generatorsWithOutput.length === 0) {
    console.log("\n  No successful generations to judge.\n");
    return { revisionNumber, slug, url, generations, judgeScores: {} };
  }

  const totalJudgeCalls = appConfig.judges.length * generatorsWithOutput.length;
  console.log(`\nJudging with ${appConfig.judges.length} judge(s) × ${generatorsWithOutput.length} generation(s) = ${totalJudgeCalls} calls in parallel…`);

  type JudgeTask = { judgeId: string; generatorId: string; promise: Promise<JudgeScoreResult> };
  const judgeTasks: JudgeTask[] = [];

  for (const judge of appConfig.judges) {
    for (const gen of generatorsWithOutput) {
      const genConfig = appConfig.generators.find((g) => g.id === gen.id)!;
      const selfEvaluation =
        genConfig.provider === judge.provider && genConfig.model === judge.model;

      const jUserPrompt = buildJudgeUserPrompt(
        url,
        productRequirements,
        gen.name,
        generations[gen.id].output!,
        hasImages
      );

      const promise = callLLM({
        provider: judge.provider,
        model: judge.model,
        systemPrompt: judgePrompt,
        userPrompt: jUserPrompt,
        maxTokens: judge.maxTokens,
        temperature: judge.temperature,
        images: hasImages ? images : undefined,
        traceContext: {
          traceName: `judge: ${judge.id} → ${gen.id}`,
          role: "judge",
          url,
          tags: ["judge", judge.id, gen.id],
        },
      })
        .then((res): JudgeScoreResult => {
          const parsed = parseJudgeResponse(res.text);
          if (parsed) {
            return {
              success: true,
              score: parsed.score,
              feedback: parsed.feedback,
              selfEvaluation,
              traceId: res.traceId,
            };
          }
          return {
            success: true,
            score: undefined,
            feedback: res.text,
            selfEvaluation,
            traceId: res.traceId,
          };
        })
        .catch(
          (err): JudgeScoreResult => ({
            success: false,
            error: err instanceof Error ? err.message : String(err),
          })
        );

      judgeTasks.push({ judgeId: judge.id, generatorId: gen.id, promise });
    }
  }

  const judgeResults = await Promise.all(judgeTasks.map((t) => t.promise));

  const judgeScores: Record<string, Record<string, JudgeScoreResult>> = {};
  judgeResults.forEach((result, i) => {
    const { judgeId, generatorId } = judgeTasks[i];
    if (!judgeScores[judgeId]) judgeScores[judgeId] = {};
    judgeScores[judgeId][generatorId] = result;
  });

  // Report judge results
  for (const judge of appConfig.judges) {
    for (const gen of generatorsWithOutput) {
      const r = judgeScores[judge.id]?.[gen.id];
      if (!r) continue;
      const selfMark = r.selfEvaluation ? " [self]" : "";
      if (r.success) {
        const scoreStr = r.score !== undefined ? `score=${r.score}` : "score=? (parse failed)";
        console.log(`  ✓ ${judge.name} → ${gen.name} — ${scoreStr}${selfMark}`);
      } else {
        console.log(`  ✗ ${judge.name} → ${gen.name} — FAILED: ${r.error}`);
      }
    }
  }

  // ── Phase 4: Patch judge scores into revision ────────────────────────────────

  const judgeScoresPatch: RevisionData["scores"]["judges"] = {};
  for (const [judgeId, genMap] of Object.entries(judgeScores)) {
    for (const [genId, r] of Object.entries(genMap)) {
      if (r.success && r.score !== undefined && r.traceId) {
        if (!judgeScoresPatch[judgeId]) judgeScoresPatch[judgeId] = {};
        judgeScoresPatch[judgeId][genId] = {
          score: r.score,
          feedback: r.feedback ?? "",
          langfuseTraceId: r.traceId,
        };
      }
    }
  }

  if (Object.keys(judgeScoresPatch).length > 0) {
    await updateRevision(slug, revisionNumber, { scores: { judges: judgeScoresPatch } });
  }

  // ── Phase 5: Send judge scores to Langfuse ───────────────────────────────────

  const scoreCalls: Promise<unknown>[] = [];
  for (const [judgeId, genMap] of Object.entries(judgeScores)) {
    for (const [genId, r] of Object.entries(genMap)) {
      if (r.success && r.score !== undefined && r.traceId) {
        scoreCalls.push(
          scoreTrace({
            traceId: r.traceId,
            name: `judge-score:${judgeId}→${genId}`,
            value: r.score,
            comment: r.feedback,
            source: "llm_judge",
          })
        );
      }
    }
  }
  await Promise.allSettled(scoreCalls);

  return { revisionNumber, slug, url, generations, judgeScores };
}

// ── Summary formatting ─────────────────────────────────────────────────────────

export function printSummary(result: ExperimentResult): void {
  const { url, revisionNumber, generations, judgeScores } = result;
  const W = 62;
  const heavy = "═".repeat(W);
  const light = "─".repeat(W);

  console.log(`\n${heavy}`);
  console.log(`  EXPERIMENT RESULTS`);
  console.log(`  URL:      ${url}`);
  console.log(`  Revision: ${revisionNumber}`);
  console.log(heavy);

  // ── Generations table ────────────────────────────────────────────────────────
  console.log("\nGENERATIONS");
  console.log(light);
  console.log(`${"Model".padEnd(32)} ${"Tokens".padStart(7)} ${"Latency".padStart(8)} ${"Chars".padStart(7)}`);
  console.log(light);

  for (const gen of appConfig.generators) {
    const r = generations[gen.id];
    if (!r) continue;
    if (!r.success) {
      console.log(`${gen.name.padEnd(32)} FAILED: ${r.error}`);
    } else {
      const tokens = (r.tokenUsage!.input + r.tokenUsage!.output).toLocaleString().padStart(7);
      const latency = `${(r.latencyMs! / 1000).toFixed(1)}s`.padStart(8);
      const chars = r.output!.length.toLocaleString().padStart(7);
      console.log(`${gen.name.padEnd(32)} ${tokens} ${latency} ${chars}`);
    }
  }

  // ── Judge scores table ───────────────────────────────────────────────────────
  const judgeIds = Object.keys(judgeScores);
  if (judgeIds.length === 0) {
    console.log("\nNo judge scores.\n");
    return;
  }

  console.log("\nJUDGE SCORES  (* = self-evaluation)");
  console.log(light);

  // Column headers: one per generator, right-aligned in 10-char cells
  const genCols = appConfig.generators.map((g) => g.name.substring(0, 9).padStart(10));
  console.log(`${"Judge".padEnd(32)} ${genCols.join(" ")}`);
  console.log(light);

  for (const judge of appConfig.judges) {
    const genMap = judgeScores[judge.id];
    if (!genMap) continue;
    const cols = appConfig.generators.map((gen) => {
      const r = genMap[gen.id];
      if (!r) return "-".padStart(10);
      if (!r.success) return "ERR".padStart(10);
      const mark = r.selfEvaluation ? "*" : " ";
      const val = r.score !== undefined ? `${r.score}${mark}` : `?${mark}`;
      return val.padStart(10);
    });
    console.log(`${judge.name.padEnd(32)} ${cols.join(" ")}`);
  }

  console.log("");
}


/**
 * CLI runner for a single experiment.
 *
 * Usage:
 *   npm run experiment <path-to-experiment.json>
 *
 * The experiment JSON must satisfy ExperimentConfig. Optional imagePaths
 * entries are read from disk and base64-encoded before being sent to models.
 */
